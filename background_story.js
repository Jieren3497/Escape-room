// You were never meant to end up here.

// Months of unpaid bills, sleepless nights, and rejected job applications slowly pushed you to the edge. Rent was overdue. Your bank account had been empty for weeks. Hope felt like something other people had.

// Then he appeared.

// A man in a black coat who called himself J.

// He approached you like he already knew everything—your debts, your desperation, your fear of failing. J spoke calmly, like a businessman offering a simple contract.

// There was a house, he said.
// An old property filled with treasures, cash, and valuables left behind long ago.

// Your job was simple.

// Go inside.
// Collect as much money as you could.
// Bring it back to him.

// In return, you would keep half of everything you found.

// Enough to erase your debts. Enough to start over.

// There was just one condition.

// J warned you the house was “not entirely safe.”

// Something lived there.

// But he brushed it off like it was nothing serious. Just an obstacle. Something that could be avoided if you were careful.

// You hesitated. Anyone would.

// But desperation has a way of silencing common sense.

// So you accepted.

// The moment you stepped inside the house, something felt wrong.

// The air was heavy.
// The walls felt like they were watching you.
// And the deeper you walked inside, the more you realized something terrible:

// This place wasn’t abandoned.

// Something impossible lives here.

// Not an animal.
// Not a person.

// Something that should not exist in this world.

// Something that hunts.

// As you search the house, you slowly uncover the truth.

// The front door won’t open.

// Not yet.

// Instead, a strange mechanism reacts to the money you collect. Each stack of cash, each piece of treasure pushes the system closer to unlocking the exit.

// The house is forcing you to keep going deeper.

// Collect enough money…
// and the door will finally open.

// Escape is possible.

// But every step toward freedom means facing the creature that lurks in the dark.

// And somewhere outside the house…

// J is waiting.

// Possible Ending Hook (for future story)

// When you finally escape, you realize something disturbing:

// J never wanted the money.

// He wanted proof that you could survive the house.

// And now…

// He has another job for you.